
public class Tire
{
    private double presure;
    private int age;
    public Tire(double presure,int age)
    {
        this.presure = presure;
        this.age = age;
    }
    public double Presure
    {
        get { return this.presure; }
    }
    public int Age
    {
        get { return this.age; }
    }
}

