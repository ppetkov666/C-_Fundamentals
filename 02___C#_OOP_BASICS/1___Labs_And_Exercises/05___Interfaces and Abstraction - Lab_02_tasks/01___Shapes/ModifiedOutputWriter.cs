using System;

public class ModifiedOutputWriter
{
    public static void WriteLine(char character)
    {
        Console.WriteLine(character);
    }

    public static void WriteLine(string textInput)
    {
        Console.WriteLine(textInput);
    }


    public static void Write(char character)
    {
        Console.Write(character);
    }

    public static void Write(string textInput)
    {
        Console.Write(textInput);
    }

    public static void WriteLine()
    {
        Console.WriteLine();
    }

    

    
}

